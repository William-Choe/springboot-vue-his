package com.cui.springboot_his.Entity;

import javax.persistence.*;

@Entity
@Table(name = "title")
public class Title {
    private int id;
    private String title;

    @Id
    @Column(name = "id")
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Basic
    @Column(name = "title")
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
